import Foundation

/**
 The type of chart being used.
 ```
 case line // Line Chart Type
 case bar // Bar Chart Type
 case pie // Pie Chart Type
 case extraLine // Extra Line Type
 ```
 */
@available(iOS 14.0, *)
public enum ChartType {
    /// Line Chart Type
    case line
    /// Bar Chart Type
    case bar
    /// Pie Chart Type
    case pie
    /// Extra Line Type
    case extraLine
}
